//Presention layer => Services => Business => Infrastructure


//Presentation layer: This is the layer that interacts with the user. It includes the user interface and handles user input and output. In a web application, this would be the controllers and views.

//Services layer: This layer contains the business logic of the application. It processes data and performs operations based on the requirements of the application. It acts as an intermediary between the presentation layer and the business layer.
//Business layer: This layer contains the core business logic and rules of the application. It defines how data is processed and how different components of the application interact with each other.
//Infrastructure layer: This layer provides the necessary infrastructure and support for the application. It includes components such as data access, logging, and other utilities that are required for the application to function properly.


//Workers layer: This layer is responsible for handling background tasks and long-running processes that are not directly related to user interactions. It can include tasks such as sending emails, processing data, or performing scheduled jobs.
